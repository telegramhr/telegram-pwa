<template>
  <div>
    <h2 class="full flex section-title">Upravo se čita</h2>
    <div class="full flex">
      <mini v-for="post in posts" :key="post.id" :post="post"></mini>
    </div>
  </div>
</template>

<script>
export default {
  name: 'Trending',
  props: {
    id: {
      type: Number,
      default: 1,
    },
  },
  data() {
    return {
      posts: [],
    }
  },
  mounted() {
    this.getPosts()
  },
  methods: {
    getPosts() {
      this.$axios
        .get(
          'https://www.telegram.hr/wp-json/telegram/pwa/v1/trending/' + this.id
        )
        .then((res) => {
          this.posts = res.data
        })
        .catch(() => {
          // TODO: error logging
        })
    },
  },
}
</script>
